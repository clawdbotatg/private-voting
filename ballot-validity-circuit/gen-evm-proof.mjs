import { readFileSync, writeFileSync } from "node:fs";
import { Noir } from "@noir-lang/noir_js";
import { UltraHonkBackend, Barretenberg } from "@aztec/bb.js";
import * as e3 from "/private/tmp/claude-501/-Users-clawd-clawd-harness-projects-slop-computer-live/5fe470df-775b-4152-a7c6-40a5241c03db/scratchpad/pkg-wasm/package/dist/node/e3_wasm.js";
const T="/private/tmp/claude-501/-Users-clawd-clawd-harness-projects-slop-computer-live/5fe470df-775b-4152-a7c6-40a5241c03db/scratchpad/interfold-repo/circuits/bin/ballot_validity/target";
const circuit = JSON.parse(readFileSync(T+"/ballot_validity.json","utf8"));
const DEG=512, TT=100n, MOD=new BigUint64Array([0xffffee001n,0xffffc4001n]);
const pk=e3.generate_public_key(DEG,TT,MOD);
const v=new BigUint64Array(DEG); v[0]=1n;
const [,j]=e3.bfv_verifiable_encrypt_vector(v,pk,DEG,TT,MOD); const a=JSON.parse(j);
const inputs={pk0is:a.pk0is,ct0is:a.ct0is,u:a.u,e0:a.e0,e0is:a.e0is,e0_quotients:a.e0_quotients,k1:a.k1,r1is:a.r1is,r2is:a.r2is,pk1is:a.pk1is,ct1is:a.ct1is,e1:a.e1,p1is:a.p1is,p2is:a.p2is};
const { witness } = await new Noir(circuit).execute(inputs);
const api=await Barretenberg.new(); await api.initSRSChonk(2**21);
const backend=new UltraHonkBackend(circuit.bytecode, api);
const { proof, publicInputs } = await backend.generateProof(witness, { verifierTarget: 'evm' });
console.log(`EVM proof: ${proof.length} bytes, ${publicInputs.length} public inputs`);
console.log("publicInputs:", publicInputs);
writeFileSync(T+"/proof.hex", "0x"+Buffer.from(proof).toString("hex"));
writeFileSync(T+"/public_inputs.json", JSON.stringify(publicInputs));
console.log("saved proof.hex + public_inputs.json");
process.exit(0);
